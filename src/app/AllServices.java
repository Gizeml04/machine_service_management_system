    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        table.setRowCount(0);
        ArrayList<Services> services;
        try
        {
            if(jRadioButton1.isSelected())
            {
                services = servicesRepo.loadServices(" type = 'консултация'");
            }
            else if(jRadioButton2.isSelected())
            {
                services = servicesRepo.loadServices(" type = 'доставка'");
            }
            else if(jRadioButton3.isSelected())
            {
                services = servicesRepo.loadServices(" type = 'ремонт'");
            }
            else 
            {
                services = servicesRepo.loadServices("");
            }
            
            for(Services service : services)
            {
                table.addRow(service.toArray());
            }
                
        }
        catch (Exception e)
        {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

