    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int row = jTable1.getSelectedRow();
       
        if(row == -1){
        JOptionPane.showMessageDialog(this,"Моля, изберете клиент от таблицата");
        return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Сигурни ли сте, че искате да изтриете този клиент?","Потвърждение",JOptionPane.YES_NO_OPTION);
        
        if(confirm != JOptionPane.YES_OPTION){
            return;
        }
        
        int clientId = Integer.parseInt(table.getValueAt(row,0).toString());
        try{
            if(clientRepo.deleteByID(clientId)){
                table.removeRow(row);
                JOptionPane.showMessageDialog(this,"Клиентът е изтрит успешно");
            }
            else {JOptionPane.showMessageDialog(this,"Грешка при изтриване");}
        }
        catch(Exception e){
        System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

