    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try 
        {
            String name = jTextField1.getText().trim();
            String address = jTextField2.getText().trim();
            String city = jTextField3.getText().trim();
            String phone = jTextField4.getText().trim();
            String email = jTextField5.getText().trim();
            

    
            if(name.isEmpty() || address.isEmpty() || city.isEmpty() || phone.isEmpty() || email.isEmpty()) 
            {
                JOptionPane.showMessageDialog(this, "Моля, попълнете всички задължителни полета!");
                return;
            }

    
            Client client = new Client(0, name, address, city, phone, email);

    
            clientRepo.insert(client);

    
            table.addRow(client.toArray());

            JOptionPane.showMessageDialog(this, "Клиентът е добавен успешно!");

    
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            

        } 
        catch (Exception e) 
        {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) 
        {
    
            int clientID = (int) table.getValueAt(selectedRow, 0);

    
            Client selectedClient = new Client(
                clientID,
                jTextField1.getText().trim(),
                jTextField2.getText().trim(),
                jTextField3.getText().trim(),
                jTextField4.getText().trim(),
                jTextField5.getText().trim()
                
            );

            try 
            {
                if (clientRepo.update(selectedClient)) 
                {
                    table.setValueAt(selectedClient.getName(), selectedRow, 1);
                    table.setValueAt(selectedClient.getAddress(), selectedRow, 2);
                    table.setValueAt(selectedClient.getCity(), selectedRow, 3);
                    table.setValueAt(selectedClient.getPhone(), selectedRow, 4);
                    table.setValueAt(selectedClient.getEmail(), selectedRow, 5);

                JOptionPane.showMessageDialog(this, "Данните за клиента са обновени!");
                } 
                else 
                {
                    JOptionPane.showMessageDialog(this, "Грешка при обновяване.");
                }
            } 
            catch (Exception e) 
            {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

